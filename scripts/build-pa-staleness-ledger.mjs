#!/usr/bin/env node
// spec-v52 §8.3 (post-52-6c follow-up): bundle the PA dataset-staleness
// ledger into the shipped JS so the in-tab report audit trail can surface
// per-source staleness without a runtime fetch (the no-network commitment,
// spec-v50 §3.1).
//
// `pa-staleness-ledger.json` (repo root) stays the single canonical,
// maintainer-edited source. This script regenerates the importable module
// `lib/pa/staleness-ledger.js` from it. `scripts/check-pa-staleness.mjs`
// verifies the two never drift (regenerate-and-diff), so CI fails if a
// maintainer edits the JSON without rebuilding the module.
//
// Run: `node scripts/build-pa-staleness-ledger.mjs`. Idempotent.

import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const LEDGER_JSON = join(ROOT, 'pa-staleness-ledger.json');
const MODULE_PATH = join(ROOT, 'lib', 'pa', 'staleness-ledger.js');

// Deterministic module text. Keep in sync with the reader in
// scripts/check-pa-staleness.mjs (renderModule).
export function renderModule(ledger) {
  return (
    '// GENERATED by scripts/build-pa-staleness-ledger.mjs from\n'
    + '// pa-staleness-ledger.json (repo root). Do not edit by hand: edit the\n'
    + '// JSON and re-run the generator (CI fails on drift via\n'
    + '// scripts/check-pa-staleness.mjs).\n'
    + '//\n'
    + '// spec-v52 §8.3: the staleness ledger bundled for the browser so the\n'
    + '// in-tab PA report audit trail can show per-source staleness with no\n'
    + '// runtime fetch (spec-v50 §3.1 no-network).\n'
    + '\n'
    + 'export const PA_STALENESS_LEDGER = '
    + JSON.stringify(ledger, null, 2)
    + ';\n'
  );
}

async function main() {
  const ledger = JSON.parse(await readFile(LEDGER_JSON, 'utf8'));
  await writeFile(MODULE_PATH, renderModule(ledger), 'utf8');
  console.log('build-pa-staleness-ledger: wrote lib/pa/staleness-ledger.js');
}

// Only run when invoked directly (`node scripts/build-pa-staleness-ledger.mjs`),
// not when `renderModule` is imported by scripts/check-pa-staleness.mjs.
if (process.argv[1] && fileURLToPath(import.meta.url) === process.argv[1]) {
  main().catch((err) => {
    console.error('build-pa-staleness-ledger: error', err);
    process.exit(2);
  });
}
